<h2>What is Accessibility?</h2>
<p>
Web Accessibility refers to the degree to which the information on a website can be properly utilized by all individuals, including
those operating under limited conditions (e.g. users with limited or no vision).  With the passage of 
<a href="http://www.section508.gov" class="popup">Section 508</a>
 of the Rehabilitation Act Amendments of 1998, accessibility has become an increasingly important aspect of web design and presentation.  For a 
more detailed introduction to Web accessibility, visit the 
<a href="http://www.webaim.org/intro/" class="popup">Web Accessibility in Mind</a>
(<abbr title="Web Accessibility In Mind">WebAIM</abbr>) website or the 
<a href="http://www.w3.org" class="popup">World Wide Web Consortium</a>
(<abbr title="World Wide Web Consortium">W3C</abbr>) website.
</p>
