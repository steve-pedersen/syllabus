    <h1>Online Syllabus Tool</h1>

    <p>Academic Technology's Syllabus tool allows faculty and academic office coordinators to build and manage class syllabi. The service draws content from campus databases and allows users to create, share, and re-use syllabi in ways that save time and allow for a paperless approach to managing this resource. Users can publish accessible versions in multiple formats including directly into iLearn courses.</p>
    
    <p>Faculty can start using this service by simply <a href="syllabus">logging in</a>.</p>
     
