    <h1>Online Syllabus Tool</h1>
    <p>
    Academic Technology's Syllabus tool allows faculty and academic office coordinators to build and manage class syllabi. This service draws content from campus databases and allows users to create, share, and re-use syllabi in ways that save time and allow for a paperless approach to managing this resource.  Users can publish accessible versions in multiple formats including directly into iLearn courses.
    </p>
    <p>
    If you are a faculty member, you may begin building your online syllabi by <a href="syllabus">Logging in</a>. If you have questions
    about <em>Syllabus</em>, you please <a href="contact">contact the <em>Syllabus</em> team</a>.
    </p>
